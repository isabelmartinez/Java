public class Bicicleta {

	//----------Propiedades---------
	public int id = 0;
	
	//----------Constructores-------
	public Bicicleta(){
	}
	
	public Bicicleta(int id){
		this.id = id;
	}
	
	//---------Getters y Settters-------
	public int getID(){
		return this.id;
	}
	
	public void setID(int id){
		this.id = id;
	}
	
}
